package com.learnbind.ai.tax.entity;

public class PreDistAccountItem {
	
	private String accountItemIds ;
	private String invoiceAmount ;
	private String invoicePrice ;
	private String invoiceTaxAmount ;
	private String invoiceWaterAmount ;
	private String taxRate ;
	
	
	public String getAccountItemIds() {
		return accountItemIds;
	}
	public void setAccountItemIds(String accountItemIds) {
		this.accountItemIds = accountItemIds;
	}
	public String getInvoiceAmount() {
		return invoiceAmount;
	}
	public void setInvoiceAmount(String invoiceAmount) {
		this.invoiceAmount = invoiceAmount;
	}
	public String getInvoicePrice() {
		return invoicePrice;
	}
	public void setInvoicePrice(String invoicePrice) {
		this.invoicePrice = invoicePrice;
	}
	public String getInvoiceTaxAmount() {
		return invoiceTaxAmount;
	}
	public void setInvoiceTaxAmount(String invoiceTaxAmount) {
		this.invoiceTaxAmount = invoiceTaxAmount;
	}
	public String getInvoiceWaterAmount() {
		return invoiceWaterAmount;
	}
	public void setInvoiceWaterAmount(String invoiceWaterAmount) {
		this.invoiceWaterAmount = invoiceWaterAmount;
	}
	public String getTaxRate() {
		return taxRate;
	}
	public void setTaxRate(String taxRate) {
		this.taxRate = taxRate;
	}
	
	
}
